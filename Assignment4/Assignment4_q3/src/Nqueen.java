	    // printing the final output or the position of queens on the board , 1 represents the positions where queen is placed ;
	    static void printSolution(int board[][]) {
	        for (int i = 0; i < N; i++) {
	            for (int j = 0; j < N; j++)
	                System.out.print(" " + board[i][j] +
	                    " ");
	            System.out.println();
	        }
	    }
	    
	    // checking if the current move is safe or not if the move is not safe it returns false ;
	    static boolean isSafe(int row, int col) {
	        int i, j;

	        for (i = 0; i < col; i++)
	            if (board[row][i] == 1)
	                return false;

	        for (i = row, j = col; i >= 0 && j >= 0; i--, j--)
	            if (board[i][j] == 1)
	                return false;

	        for (i = row, j = col; j >= 0 && i < N; i++, j--)
	            if (board[i][j] == 1)
	                return false;

	        return true;
	    }
	    
	    //recursive function which places or arranges each queen in a column ;
	    static boolean solveRec(int col) {
	        if (col == N)
	            return true;

	        for (int i = 0; i < N; i++) {
	            if (isSafe(i, col)) {
	                board[i][col] = 1;

	                if (solveRec(col + 1) == true)
	                    return true;

	                board[i][col] = 0;
	            }
	        }
	        return false;
	    }
	    
